    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
                    try {
            String sql = "UPDATE tbl_barang SET kd_barang= '"+txt_kodebarang.getText()+"', "
                    + "nma_barang = '"+txt_namabarang.getText()+"', stok = '"+txt_stok.getText()+"', "
                    + "hrga_barang = '"+txt_hargabarang.getText()
                    +"' WHERE kd_barang = '"+txt_kodebarang.getText()+"'";
            java.sql.Connection conn=(Connection)Connect.GetConnection();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "perubahan data gagal" + e.getMessage());
        }
        datatable();
    }//GEN-LAST:event_btn_ubahActionPerformed

