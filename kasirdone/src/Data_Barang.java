    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String kd_barang = txt_kodebarang.getText();
        String nma_barang = txt_namabarang.getText();
        String stok = txt_stok.getText();
        String hrga_barang = txt_hargabarang.getText();
        
        
        try {
            Statement statement = (Statement) Connect.GetConnection().createStatement();
            statement.executeUpdate("insert into tbl_barang VALUES ('" + kd_barang + "','" + nma_barang + "','" + stok + "','" + hrga_barang + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan");
        }
        datatable();
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
              String kd_barang = txt_kodebarang.getText();
        try {
            Statement statement = (Statement) Connect.GetConnection().createStatement();
            statement.executeUpdate("DELETE from tbl_barang where kd_barang =('" + kd_barang + "');");
            JOptionPane.showMessageDialog(null, "data berhasil di HAPUS");
            txt_kodebarang.setText("");
            txt_namabarang.setText("");
            
            txt_stok.setText("");
            txt_hargabarang.setText("");
            txt_kodebarang.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal di HAPUS");
        }
        datatable();
    }//GEN-LAST:event_btn_hapusActionPerformed

